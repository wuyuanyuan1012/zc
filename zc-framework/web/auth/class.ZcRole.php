<?php
class ZcRole {
	private $zcRoleId;
	private $name;
	private $desc;
	private $subRoleIds;
	private $permissionIds;

	public function __construct($zcRoleId, $name, $desc, $subRoleIds, $permissionIds) {
		$this->zcRoleId = $zcRoleId;
		$this->name = $name;
		$this->desc = $desc;
		$this->subRoleIds = $subRoleIds;
		$this->permissionIds = $permissionIds;
	}

	public function getZcRoleId() {
		return $this->zcRoleId;
	}

	public function setZcRoleId($zcRoleId) {
		$this->zcRoleId = $zcRoleId;
		return $this;
	}

	public function getName() {
		return $this->name;
	}

	public function setName($name) {
		$this->name = $name;
		return $this;
	}

	public function getDesc() {
		return $this->desc;
	}

	public function setDesc($desc) {
		$this->desc = $desc;
		return $this;
	}

	public function getSubRoleIds() {
		return $this->subRoleIds;
	}

	public function setSubRoleIds($subRoleIds) {
		$this->subRoleIds = $subRoleIds;
		return $this;
	}

	public function getPermissionIds() {
		return $this->permissionIds;
	}

	public function setPermissionIds($permissionIds) {
		$this->permissionIds = $permissionIds;
		return $this;
	}
}
